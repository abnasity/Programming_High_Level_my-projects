# Creating a set
fruits = {"apple", "banana", "cherry", "apple"}

print(fruits)  # Output: {'banana', 'cherry', 'apple'} (no duplicates)

# Add an item
fruits.add("orange")

# Remove an item
fruits.remove("banana")

# Check if an item is in the set
print("apple" in fruits)  # True